/* Insight (1)
 * Indices (index): são gerados AUTOMATICAMENTE em um Array [0] [1] [2] [3]
 */

/* Insight (2)
 * VSCode (IDE) .Propriedade (barra de ouro AZUL)
 * VSCode (IDE) .Metodo() (cubo magico ROXO)
 */

/* Insight (3)
 * Math (Objeto nativo do JS). Colocando o objeto e apertando o enter no console do browser (navegador) você tem acesso as Propriedades e aos Metodos.
 * Math.PI (Objeto acessando uma Propriedade, obs: variavel).
 * Math.abs(5) (Objeto acessando um Método e colocando um valor para retornar).
 * console.log(Math.abs(5)); (Retorna o valor 5 no console do navegador).
 */

/* Insight (4)
 * console (Objeto nativo do JS). Colocando o objeto e apertando o enter no console você tem acesso aos Metodos.
 * console.log() (Objeto acessando um Método, obs: função).
 */

/* Insight (5)
 * GET (acessa, pega, puxa um valor para mostrar no "front-end") objeto.propriedade || objeto.metodo()
 * SET (atribui, adiciona, insere, injeta um novo valor no "back-end") objeto.propriedade = "novo valor";
 * this (acessa, pega, puxa tudo que tem dentro do próprio Objeto) this.propriedade || this.metodo()
 */

/* Insight (6)
 * Propriedade (variavel dentro do Objeto)
 * Método (função dentro do Objeto)
 * Objeto (possui Propriedades e Métodos dentro dele)
 * Para facilitar: tudo em JavaScript é objeto. Na grande maioria dos casos, vamos ter que acessar as propriedades e métodos de um objeto JS nativo.
 */

/* Insight (7)
 * Na verdade, saber / entender bem as Propriedades e Métodos dos diferentes Objetos do JS
 * é que é possível compreender o JS.
 */

/* Insight (8)
 * Função (sempre retorna um valor, para executar uma função precisa do () parenteses)
 */

/* Insight (9)
 * Quando uma função recebe um parametro, exemplo: function mulher(parametro){ return parametro }
 * é porque precisa receber um argumento (input, entrada), exemplo: mulher("I love you")
 * console.log(mulher("I love you")); // Função ativada que recebe o argumento: "I love you" e 
 * retorna a frase: I love you no console do browser (navegador). :)
 */

