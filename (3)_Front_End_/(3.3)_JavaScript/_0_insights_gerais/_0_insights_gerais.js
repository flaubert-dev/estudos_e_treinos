/* Insight (1)
 * Indices (index): são gerados AUTOMATICAMENTE em um Array [0] [1] [2] [3]
 */

/* Insight (2)
 * Math (Objeto nativo do JS). Colocando o objeto e apertando o enter no console do browser (navegador) você tem acesso aos Atributos (propriedades) e aos Metodos.
 * Math.PI (Objeto acessando um atributo (propriedade), obs: variavel).
 * Math.abs(5) (Objeto acessando um Método e colocando um valor para retornar).
 * console.log(Math.abs(5)); (Retorna o valor 5 no console do navegador).
 */

/* Insight (3)
 * VSCode (IDE) .Atributo ou Propriedade (barra AZUL)
 * VSCode (IDE) .Metodo() (cubo magico ROXO)
 */

/* Insight (4)
 * Atributo ou propriedade: variavel dentro do Objeto
 * Método: função dentro do Objeto
 * Objeto: possui Atributos (propriedades) e Métodos dentro dele.
 * Para facilitar: tudo em JavaScript é objeto. Na grande maioria dos casos, vamos ter que acessar os atributos e métodos de um objeto JS nativo.
 */

/* Insight (5)
 * Na verdade, saber / entender bem os Atributos (propriedades) e Métodos dos diferentes Objetos do JS
 * é que é possível compreender o JS.
 */

/* Insight (6)
 * console (Objeto nativo do JS). Colocando o objeto e apertando o enter no console você tem acesso aos Metodos.
 * console.log() (Objeto acessando um Método, obs: função).
 */

/* Insight (7)
 * GET (acessa, pega, puxa um valor) objeto.atributo || objeto.metodo()
 * SET (altera, atribui, adiciona, insere, injeta um novo valor) objeto.atributo = "novo valor";
 * this (acessa, pega, puxa tudo que tem dentro do próprio Objeto) this.atributo || this.metodo()
 */

/* Insight (8)
 * Função (sempre retorna um valor, para executar uma função precisa do () parenteses)
 */

/* Insight (9)
 * Quando uma função recebe um parametro, exemplo: function mulher(parametro){ return parametro }
 * é porque precisa receber um argumento (input, entrada), exemplo: mulher("I love you")
 * console.log(mulher("I love you")); // Função ativada que recebe o argumento: "I love you" e 
 * retorna a frase: I love you no console do browser (navegador). :)
 */

